  // ===== Language name labels (self-referencing) =====
  const LANG_LABELS = { en: "English", cs: "Čeština", de: "Deutsch" };

  // ===== t() function =====
  function t(key) {
    const d = _current === "cs" ? cs : _current === "de" ? de : en;
    let s = d[key] || en[key] || key;
    // Interpolation: replace {0}, {1}, ... with extra arguments
    for (let i = 1; i < arguments.length; i++) {
      s = s.split("{" + (i - 1) + "}").join(String(arguments[i]));
    }
    return s;
  }

  function setLang(lang) {
    if (!SUPPORTED.includes(lang)) return;
    _current = lang;
    localStorage.setItem(LS_KEY, lang);
    document.documentElement.lang = lang;
  }

  function getLang() {
    return _current;
  }

  // Apply lang attribute on load
  document.documentElement.lang = _current;

  // Public API
  window.RFQ_LANG = {
    t: t,
    setLang: setLang,
    getLang: getLang,
    SUPPORTED: SUPPORTED,
    LANG_LABELS: LANG_LABELS,
  };

  // Shortcut: also expose t() globally for convenience
  window.t = t;
